function runGuides(data) {
    const guidesShow = document.getElementById("guidesShow");
    if (!guidesShow) return;
    const guidesDiv = data.map((element) => {
        return `
        <div class="rounded-md overflow-hidden bg-white flex flex-col">
            <div class="h-[50%] overflow-hidden">
            <img src="image/${element.image}" alt="" class="w-full object-cover">
            </div>
            <div class=" flex flex-col p-4 gap-3 mt-auto">
                <h4 class="text-lg font-semibold" >${element.title}</h4>
                <p>${element.summary}</p>
                <button class="btn btn-primary ml-auto my-3" onclick="my_modal_1.showModal()">View details</button>
            </div>
        </div>`
    }).join("");
    guidesShow.innerHTML = guidesDiv;
}

function guidesFilter() {
    const categoryValue = document.getElementById("catagoryValue").value;
    const seasonValue = document.getElementById("seasonVlaue").value;
    const methodValue = document.getElementById("methodVlaue").value;

    const filtered = guides.filter((element) => {
        const matchCategory = categoryValue === "All" || element.category === categoryValue;
        const matchSeason = seasonValue === "All" || element.season === seasonValue;
        const matchMethod = methodValue === "All" || element.method === methodValue;
        return matchCategory && matchSeason && matchMethod;
    });

    runGuides(filtered);
}

runGuides(guides);



